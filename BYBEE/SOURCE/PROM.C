void bootrom( void )
	{
	S16 i, j;
	U8 *p;

	if (poly88_flag)
		p = rom40;
	else
		p = rom;

	for (i = 0; i < sizeof(rom); ++i)
		ram[i] = *p++;

	if (poly88_flag && tbasic_flag)
		{
		p = tbasic;
		j = 1024;
		for (i = 0; i < sizeof(tbasic); ++i)
			ram[j++] = *p++;
		}
	}


